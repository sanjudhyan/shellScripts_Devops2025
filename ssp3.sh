#!/bin/bash
echo "Enter the first number"
read num1
echo "Enter the second number"
read num2

product=$((num1*num2))
echo "The product of two numbers is $product"

sum=$((num1+num2))
echo "The sum of two numbers is $sum"

modulo=$((num1%num2))
echo "The modulo of two number is $modulo"

subtraction=$((num1-num2))
echo "The subtraction of two numbers is $subtraction"

